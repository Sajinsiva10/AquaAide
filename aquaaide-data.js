function calculateDistance(lat1, lng1, lat2, lng2) {
  const R = 6371;
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a = Math.sin(dLat/2) * Math.sin(dLat/2) +
            Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
            Math.sin(dLng/2) * Math.sin(dLng/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  const distance = R * c;
  return Math.round(distance * 1000);
}

function getNearestServices(userLat, userLng, limit = 10) {
  return AQUAAIDE_SERVICES
    .map(service => ({
      ...service,
      distance: calculateDistance(userLat, userLng, service.lat, service.lng)
    }))
    .sort((a, b) => a.distance - b.distance)
    .slice(0, limit)
    .map(service => ({
      ...service,
      distanceText: service.distance < 1000 ? 
        `${service.distance}m` : 
        `${(service.distance / 1000).toFixed(1)}km`
    }));
}

